package com.nrlm.melasalesoffline.pojo;

public class ShgBillModel {

    String shgRegId;
    String shgName;
    String totalPrice;
    String mobileNumber;
    String sumInvoiceNo;

    public String getShgRegId() {
        return shgRegId;
    }

    public void setShgRegId(String shgRegId) {
        this.shgRegId = shgRegId;
    }

    public String getShgName() {
        return shgName;
    }

    public void setShgName(String shgName) {
        this.shgName = shgName;
    }

    public String getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(String totalPrice) {
        this.totalPrice = totalPrice;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getSumInvoiceNo() {
        return sumInvoiceNo;
    }

    public void setSumInvoiceNo(String sumInvoiceNo) {
        this.sumInvoiceNo = sumInvoiceNo;
    }
}
